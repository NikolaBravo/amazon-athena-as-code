package com.github.vitalibo.a3c.provisioner;

public class AthenaResourceProvisionException extends Exception {

    public AthenaResourceProvisionException() {
        super();
    }

    public AthenaResourceProvisionException(String message) {
        super(message);
    }

    public AthenaResourceProvisionException(String message, Throwable cause) {
        super(message, cause);
    }

    public AthenaResourceProvisionException(Throwable cause) {
        super(cause);
    }

}
