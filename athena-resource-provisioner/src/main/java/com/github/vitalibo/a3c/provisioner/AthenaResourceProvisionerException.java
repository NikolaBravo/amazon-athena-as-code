public class AthenaResourceProvisionerException extends Exception {

    public AthenaResourceProvisionerException() {
        super();
    }

    public AthenaResourceProvisionerException(String message) {
        super(message);
    }

    public AthenaResourceProvisionerException(String message, Throwable cause) {
        super(message, cause);
    }

    public AthenaResourceProvisionerException(Throwable cause) {
        super(cause);
    }

}
